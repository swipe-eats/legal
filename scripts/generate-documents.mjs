import { mkdir, readFile, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const contentDir = path.join(root, "content");
const generatedDir = path.join(root, "src", "generated");

const documents = [
	{
		input: path.join(contentDir, "terms-and-conditions.md"),
		output: path.join(generatedDir, "terms-and-conditions.ts"),
		exportName: "termsAndConditions",
		versionName: "TERMS_VERSION",
	},
	{
		input: path.join(contentDir, "privacy-policy.md"),
		output: path.join(generatedDir, "privacy-policy.ts"),
		exportName: "privacyPolicy",
		versionName: "PRIVACY_VERSION",
	},
];

const checkOnly = process.argv.includes("--check");

await mkdir(generatedDir, { recursive: true });

let hasChanges = false;

for (const document of documents) {
	const markdown = await readFile(document.input, "utf8");
	const parsed = parseMarkdownDocument(markdown, document.input);
	const generated = renderDocumentModule(parsed, document);
	const current = await readOptionalFile(document.output);

	if (current !== generated) {
		hasChanges = true;

		if (checkOnly) {
			console.error(`${path.relative(root, document.output)} is out of date`);
		} else {
			await writeFile(document.output, generated, "utf8");
		}
	}
}

if (checkOnly && hasChanges) {
	process.exitCode = 1;
}

async function readOptionalFile(filePath) {
	try {
		return await readFile(filePath, "utf8");
	} catch (error) {
		if (error?.code === "ENOENT") {
			return undefined;
		}

		throw error;
	}
}

function parseMarkdownDocument(markdown, filePath) {
	const normalized = markdown.replaceAll("\r\n", "\n").replaceAll("\r", "\n");
	const frontmatterMatch = normalized.match(/^---\n([\s\S]*?)\n---\n?/);

	if (!frontmatterMatch) {
		throw new Error(`${filePath} is missing frontmatter`);
	}

	const metadata = parseFrontmatter(frontmatterMatch[1], filePath);
	const body = normalized.slice(frontmatterMatch[0].length);
	const lines = body.split("\n");
	const intro = [];
	const sections = [];
	let currentSection = null;
	let index = 0;

	while (index < lines.length) {
		const line = lines[index];

		if (line.trim() === "") {
			index += 1;
			continue;
		}

		if (line.startsWith("# ")) {
			index += 1;
			continue;
		}

		if (line.startsWith("## ")) {
			currentSection = {
				heading: line.slice(3).trim(),
				blocks: [],
			};
			sections.push(currentSection);
			index += 1;
			continue;
		}

		if (line.startsWith("### ")) {
			const targetBlocks = currentSection ? currentSection.blocks : intro;
			targetBlocks.push({
				type: "subheading",
				text: line.slice(4).trim(),
			});
			index += 1;
			continue;
		}

		const parsedBlock = line.startsWith("- ")
			? parseList(lines, index)
			: parseParagraph(lines, index);

		const targetBlocks = currentSection ? currentSection.blocks : intro;
		targetBlocks.push(parsedBlock.block);
		index = parsedBlock.nextIndex;
	}

	return {
		...metadata,
		intro,
		sections,
	};
}

function parseFrontmatter(frontmatter, filePath) {
	const metadata = {};

	for (const line of frontmatter.split("\n")) {
		if (line.trim() === "") {
			continue;
		}

		const match = line.match(/^([A-Za-z][A-Za-z0-9]*):\s*(.*)$/);
		if (!match) {
			throw new Error(`${filePath} has invalid frontmatter line: ${line}`);
		}

		metadata[match[1]] = stripQuotes(match[2].trim());
	}

	for (const key of ["title", "slug", "version", "effectiveDate"]) {
		if (!metadata[key]) {
			throw new Error(`${filePath} is missing frontmatter key: ${key}`);
		}
	}

	return metadata;
}

function stripQuotes(value) {
	if (
		(value.startsWith('"') && value.endsWith('"')) ||
		(value.startsWith("'") && value.endsWith("'"))
	) {
		return value.slice(1, -1);
	}

	return value;
}

function parseList(lines, startIndex) {
	const items = [];
	let index = startIndex;

	while (index < lines.length && lines[index].startsWith("- ")) {
		items.push(lines[index].slice(2).trim());
		index += 1;
	}

	return {
		block: {
			type: "list",
			items,
		},
		nextIndex: index,
	};
}

function parseParagraph(lines, startIndex) {
	const paragraphLines = [];
	let index = startIndex;

	while (index < lines.length) {
		const line = lines[index];

		if (
			line.trim() === "" ||
			line.startsWith("# ") ||
			line.startsWith("## ") ||
			line.startsWith("### ") ||
			line.startsWith("- ")
		) {
			break;
		}

		paragraphLines.push(line.trim());
		index += 1;
	}

	return {
		block: {
			type: "paragraph",
			text: paragraphLines.join(" "),
		},
		nextIndex: index,
	};
}

function renderDocumentModule(document, config) {
	return `import type { LegalDocument } from "../types.js";

// This file is generated by scripts/generate-documents.mjs.
// Edit content/${document.slug}.md instead.
export const ${config.exportName} = ${renderValue(document)} as const satisfies LegalDocument;

export const ${config.versionName} = ${config.exportName}.version;
`;
}

function renderValue(value, indent = 0) {
	const currentIndent = "\t".repeat(indent);
	const nextIndent = "\t".repeat(indent + 1);

	if (Array.isArray(value)) {
		if (value.length === 0) {
			return "[]";
		}

		const items = value
			.map((item) => `${nextIndent}${renderValue(item, indent + 1)}`)
			.join(",\n");

		return `[\n${items},\n${currentIndent}]`;
	}

	if (value && typeof value === "object") {
		const entries = Object.entries(value)
			.map(
				([key, entryValue]) =>
					`${nextIndent}${key}: ${renderValue(entryValue, indent + 1)}`,
			)
			.join(",\n");

		return `{\n${entries},\n${currentIndent}}`;
	}

	return JSON.stringify(value);
}
